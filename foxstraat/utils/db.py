def cast_vote(bulletin, vote_value, vote):
    vote.has_voted = True
    # Upvote
    if vote_value == 1:
        vote.value = vote_value
        bulletin.upvotes = bulletin.upvotes + 1

        # Calculate score
        age = bulletin.date_created
        score = hot(ups=bulletin.upvotes, downs=bulletin.downvotes, date=age)
        bulletin.score = bulletin.score + score
        bulletin.save()
        vote.save()
    # Downvote
    elif vote_value == -1:
        vote.value = vote_value
        bulletin.downvotes = bulletin.downvotes + 1

        # Calculate score
        age = bulletin.date_created
        score = hot(ups=bulletin.upvotes, downs=bulletin.downvotes, date=age)
        bulletin.score = bulletin.score - score

        bulletin.save()
        vote.save()
    # Cancel vote
    elif vote_value == 0:
        # If user previously downvoted the post
        if vote.value == -1:
            vote.value = 0
            bulletin.downvotes = bulletin.downvotes - 1

            # Calculate score
            age = bulletin.date_created
            score = hot(ups=bulletin.upvotes, downs=bulletin.downvotes, date=age)
            bulletin.score = bulletin.score - score

            bulletin.save()
            vote.save()

        # If user previously upvoted the post
        elif vote.value == 1:
            vote.value = 0
            bulletin.upvotes = bulletin.upvotes - 1

            # Calculate score
            age = bulletin.date_created
            score = hot(ups=bulletin.upvotes, downs=bulletin.downvotes, date=age)
            bulletin.score = bulletin.score - score

            bulletin.save()
            vote.save()
        elif vote.value == 0:
            pass

    else:
        return HttpResponseBadRequest()


